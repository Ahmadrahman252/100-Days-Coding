
public class typedata_primitif_aritmatika_int {
    public static void main(String[] args) {
        int nilai1 = 20;
        int nilai2 = 30;
        int hasil1 = nilai1 + nilai2;
        int hasil2 = nilai1 * nilai2;
        int hasil3 = nilai1 - nilai2;
        int hasil4 = nilai1 / nilai2;
        int hasil5 = nilai1 % nilai2;
        
        System.out.println("Hasil penjumlahan: " + hasil1);
        System.out.println("Hasil kali: " + hasil2);
        System.out.println("Hasil kurang: " + hasil3);
        System.out.println("Hasil bagi: " + hasil4);
        System.out.println("Hasil modulus sisa bagi: " + hasil5);
    }
}
