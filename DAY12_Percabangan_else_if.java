public class percabangan_if_else {
    public static void main(String[] args) {
        int nilai = 81;
        if (nilai >= 90) {
            System.out.println("Nilai : A");
        }
         else if (nilai >= 75) {
            System.out.println("Nilai : B");
        }
         else if (nilai >= 65) {
            System.out.println("Nilai : C");
        }
         else {
            System.out.println("Nilai : D");
        }
    }
}
